package model;

public class Funcionarios {

	
	private String nome;
	private String setor;
	private String funcao;
	
	
	
	public Funcionarios(String nome, String setor, String funcao) {
		super();
		this.nome = nome;
		this.setor = setor;
		this.funcao = funcao;
	}



	public String getNome() {
		return nome;
	}



	public String getSetor() {
		return setor;
	}



	public String getFuncao() {
		return funcao;
	}
	
	
	
	
	
	
	
	

}
